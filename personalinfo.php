個人情報の第三者提供<br><br>

当社は、ご提供いただいた個人情報を次の場合を除き第三者に開示・提供いたしません。<br>
・ご本人の同意がある場合<br>
・法令に基づく場合<br>
・人の生命、身体又は財産の保護のために必要がある場合であって、人の同意を得ることが困難であるとき<br>
・公衆衛生の向上又は児童の健全な育成の推進のために特に必要がある場合であって本人の、同意を得ることが困難であるとき<br>
・国の機関若しくは地方公共団体又はその委託を受けた者が法令の定める事務を遂行することに対して協力する必要がある場合であって、本人の同意を得ることによって当該事務の遂行に支障を及ぼすおそれがあるとき<br><br>
 

個人情報取扱いの委託<br><br>

当社は、事業運営上、お客様により良いサービスを提供するために業務の一部を外部に委託しています。業務委託先に対しては、個人情報を預けることがあります。この場合、個人情報を適切に取り扱っていると認められる委託先を選定し、契約等において個人情報の適正管理・機密保持などによりお客様の個人情報の漏洩防止に必要な事項を取決め、適切な管理を実施させます。<br><br>
 

