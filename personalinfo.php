<br>

受付システムの開発、受付業務のコンサルティングを主力事業とする当社は、個人情報は、個人の人格尊重の理念の下に慎重に取り扱われるべきものと考え、個人情報保護に取り組むことを当社の重要な社会的使命と認識し、役員はじめ全従業者が当社の事業で取り扱う個人情報及び当社従業者等の個人情報に関して、個人情報保護に関する法規範を遵守するためJISQ15001に準拠した個人情報保護マネジメントシステムを構築して適切に運用し、常に社会的要請の変化に着目しつつ個人情報保護マネジメントシステムの継続的改善に全社を挙げて取り組むことを宣言いたします。<br><br>

 

１． 当社はクラウド型受付システム「RECEPTIONIST」の導入に関する事業等、並びに従業者の雇用、人事管理等において取扱う個人情報について、適切な取得・利用及び提供をし、特定された利用目的の達成に必要な範囲を超えた個人情報の取扱いを行ないません。また、そのための措置を講じます。<br><br>
 

２． 当社は個人情報の取扱いに関する法令、国が定める指針その他の規範を遵守致します。<br><br>
 

３． 当社は個人情報の漏えい、滅失、き損などのリスクに対しては、合理的な安全対策を講じて防止すべく、事業の実情に合致した経営資源を注入し、個人情報のセキュリティ体制を継続的に向上させていきます。また、万一の際には速やかに是正措置を講じます。<br><br>
 

４． 当社は個人情報取扱いに関する苦情及び相談に対しては、迅速かつ誠実に対応致します。<br><br>
 

５． 個人情報保護マネジメントシステムは、当社を取り巻く環境の変化と実情を踏まえ、適時・適切に見直して継続的に改善をはかります。<br><br>
 

本方針は、全ての従業者に配付して周知させるとともに、当社のホームページ、パンフレット等に掲載することにより、いつでもどなたにも入手可能な措置を取るものとします。<br><br>


